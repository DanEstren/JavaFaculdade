public abstract class Atributos {

    protected double juros;
    protected int tempo;
    protected double capital;
    protected double aportes;

    Atributos(double juros,int tempo, double capital, double aportes){
        this.juros = juros;
        this.tempo = tempo;
        this.capital = capital;
        this.aportes = aportes;

    }

    
    
}
